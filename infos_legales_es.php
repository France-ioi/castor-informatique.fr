<h1>PDP - Protección de datos personales</h1>
<p>
Cuando las instituciones y los usuarios participan en las actividades de QuickPi, se recopila información personal.
</p>
<p>
France-ioi se compromete a cumplir con todas las leyes y regulaciones sobre la protección de los datos así recopilados.
</p>
<h1> Datos recopilados y propósito de la recopilación </h1>

<h3> Participantes en los concursos Castor y Algoréa </h3>
<b> Datos recopilados (obligatorios): </b>
<ul>
   <li> Nombre y apellido <br/>
   (se puede proporcionar un seudónimo o un nombre parcial) </li>
   <li> Nivel escolar </li>
   <li> Escuela (indirectamente) </li>
   <li> Respuestas a los ejercicios </li>
</ul>
<br/>
<b> Objeto de la colección: </b>
<ul>
   <li> Visualización de los resultados de los estudiantes en la interfaz del coordinador de la escuela </li>
   <li> Identificación de los participaciones afectados por una anomalía reportada por el profesor </li>
</ul>

<h3> Profesores que coordinan la actividad </h3>
<b> Datos recopilados (obligatorios): </b>
<ul>
   <li> Nombre y apellido </li>
   <li> Título (Sr., Sra., ...) </li>
   <li> Direcciones de correo electrónico </li>
   <li> Datos de contacto de la escuela </li>
</ul>
<br/>
<b> Objeto de la colección: </b>
<ul>
   <li> Verificación del estado de maestro </li>
   <li> Comunicación directa con el maestro </li>
   <li> Publicación del número de participantes por escuela </li>
</ul>

<p>
La información personal transmitida por los usuarios es registrada en un archivo computarizado por la asociación France-ioi.
</p>

<p>Durante la participación individual en una actividad de acceso libre, se guardan las respuestas usuario a los ejercicios, y son vinculadas a un código generado aleatoriamente, que se trasmite al usuario. </p>

<p>Esto tiene como finalidad permitir que el usuario continúe con su actividad más tarde si ingresa dicho código.</p>
<p>Otra finalidad es crear estadísticas del uso del contenido, la dificultad de los ejercicios, etc. y está destinado a la mejora futura de los contenidos.</p>

<p>Estos datos no contienen información personal, salvo si el usuario elije añadirla a las respuestas de los ejercicios, a pesar que esto no sea pedido ni sugerido. Sin embargo, esta situación es posible, y por ello tratamos de que todos los datos registrados tengan las mismas condiciones de seguridad como si se trataran de datos de carácter personal.</p>

<p>
Los datos ingresados por los usuarios son registrados en un archivo digitalizado por la asociación France-ioi.
</p>
<h1>Duración de la conservación de datos</h1>
<p>
Las respuestas de los estudiantes a los ejercicios son conservadas por 18 meses como máximo.</p>

<h1>Confidencialidad y seguridad de los datos</h1>
<p>
Durante este período, utilizamos todos los medios posibles para asegurar la confidencialidad y seguridad de los datos, para evitar su daño, eliminación o el acceso a terceros no autorizados.</p>
<p>
El acceso a estos datos está limitado estrictamente a un máximo de 3 personas que trabajen para la asociación France-ioi, habilitados según sus funciones y sometidos a un régimen de confidencialidad: el presidente de la asociación y dos desarrolladores encargados del mantenimiento de los servidores...</p>
<p>
France-ioi se compromete a no vender, prestar, ceder o regalar el acceso a los datos personales a terceros, a menos que se tenga un motivo legítimo (obligación legal, lucha contra el fraude o abuso, ejercicio de derecho a la defensa, etc.).
</p>
<p>
Los datos personales utilizados en el marco de nuestras actividades son guardados solamente en los servidores respaldados por la arquitectura del líder mundial de almacenamiento de servidores, AWS, localizados en Europa. Los datos jamás serán transferidos a un país fuera del Espacio Económico Europeo (EEE)
</p>
<p>
El acceso a los datos personales se hace siempre mediante una conexión segura. 
</p>
<h1>Derechos de acceso, rectificación, eliminación y portabilidad</h1>
<p>
De acuerdo a las disposiciones legales y reglamentarias aplicables, en particular la ley n° 78-17 del 6 enero de 1978 con la modificación sobre la informática, los archivos y las libertades y el reglamento europeo n°2016/679/UE del 27 de abril de 2016 (aplicable a partir del 25 de mayo de 2018), el usuario dispone de los siguientes derechos:
</p>
<ul>
   <li>Ejercer su derecho de acceso, para conocer los datos personales que le competan ;</li>
   <li>Exigir la actualización de sus datos, si son inexactos ;</li>
   <li>Exigir la portabilidad o la eliminación de sus datos ;</li>
   <li>Exigir la limitación del uso de sus datos ;</li>
   <li>Negar, por motivos legítimos, el uso de sus datos ;</li>
</ul>
<p>
Si desea ejercer estos derechos, comuníquese por el siguiente correo electrónico: info@france-ioi.org.
</p>
<p>
Ya que no se almacena información personal, solo puede darnos el código aleatorio de usuario para que podamos identificar cuáles son los datos pertinentes a la solicitud. 
</p>
