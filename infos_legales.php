<h1>RGPD - Protection des données à caractère personnel</h1>
<p>
Lors de la participation d’établissement et d’utilisateurs aux concours Castor et Algoréa, des informations personnelles sont collectées.
</p>
<p>
France-ioi s'engage à respecter toutes les lois et réglementations sur la protection des données ainsi collectées.
</p>
<h1>Données collectées, et finalité de la collecte</h1>

<h3>Participants aux concours Castor et Algoréa</h3>
<b>Données collectées (requises) :</b>
<ul>
   <li>Nom, prénom<br/>
   (un pseudonyme ou nom partiel peuvent être fournis, au détriment des diplômes)</li>
   <li>Niveau scolaire</li>
   <li>Établissement scolaire (indirectement)</li>
   <li>Réponses aux exercices du concours et des entraînements</li>
   <li>Résultats de leur participation au concours</li>
</ul>
<br/>
<b>Finalité de la collecte :</b>
<ul>
   <li>Impression de diplômes nominatifs par les enseignants</li>
   <li>Calcul du classement par niveau scolaire</li>
   <li>Calcul du classement par établissement</li>
   <li>Affichage des résultats des élèves dans l’interface du coordinateur de l’établissement</li>
   <li>Identification des participations concernées par une anomalie remontée par l’enseignant</li>
</ul>

<h3>Participants à la demi-finale Algoréa</h3>
<b>Données supplémentaires collectées (requises) :</b>
<ul>
   <li>Adresse email de contact</li>
</ul>
<br/>
<b>Finalité de la collecte :</b>
<ul>
   <li>Prise de contact pour l’annonce éventuelle de sélection en finale</li>
</ul>
<h3>Enseignants qui coordonnent le concours</h3>
<b>Données collectées (requises) :</b>
<ul>
   <li>Nom, prénom</li>
   <li>Genre</li>
   <li>Adresses email</li>
   <li>Coordonnées de l’établissement</li>
</ul>
<br/>
<b>Finalité de la collecte :</b>
<ul>
   <li>Vérification du statut d’enseignant</li>
   <li>Communication directe avec l'enseignant</li>
   <li>Impression des diplômes</li>
   <li>Envoi de documents à l’établissement</li>
   <li>Publication du nombre de participants par établissement</li>
</ul>

<p>
Les informations personnelles transmises par les utilisateurs des concours Castor et Algoréa sont enregistrées dans un fichier informatisé par l'association France-ioi.
</p>
<p>
<!-- Lorsque certaines informations sont obligatoires pour accéder à des fonctionnalités spécifiques du site, ce caractère obligatoire est indiqué au moment de la saisie des données. --> En cas de refus de la part d'un enseignant de fournir des informations requises pour permettre la validation de son statut d'enseignant et l'identification de son établissement, il pourrait ne pas obtenir l'accès à la coordination du concours.
</p>
<p>
En cas de refus de la part d'un participant de fournir les informations requises, il pourrait ne pas obtenir l'accès à la demi-finale et à la finale du concours Algoréa.
</p>
<h1>Durée de conservation des données</h1>
<p>
Les données personnelles des élèves ayant participé aux concours sans créer explicitement de compte personnel sont conservées au maximum 18 mois.
</p>
<p>
Les données personnelles des personnes ayant créé un compte personnel pour participer à la demi-finale sont conservées au maximum pendant 3 ans après leur dernière connexion au site.
</p>

<h1>Confidentialité et sécurité des données</h1>
<p>
Pendant cette période, nous mettons en place tous moyens aptes à assurer la confidentialité et la sécurité de vos données personnelles, de manière à empêcher leur endommagement, effacement ou accès par des tiers non autorisés.
</p>
<p>
L'accès aux données personnelles des utilisateurs des concours Castor Informatique et Algoréa est strictement limité à un maximum de 3 personnes travaillant pour l’association France-ioi, habilités en raison de leurs fonctions et tenus à une obligation de confidentialité : le président de l'association et deux développeurs responsables de la maintenance des serveurs..
</p>
<p>
France-ioi s'engage à ne pas vendre, louer, céder ou donner accès à des tiers aux données personnelles sans consentement préalable des personnes concernées, à moins d’y être contraints en raison d’un motif légitime (obligation légale, lutte contre la fraude ou l’abus, exercice des droits de la défense, etc.).
</p>
<p>
Les données personnelles traitées dans le cadre de nos activités sont stockées uniquement sur des serveurs sécurisés de l’architecture du leader mondial d'hébergement de serveurs, AWS, localisés en europe. Les données ne seront jamais transférées vers un pays en dehors de l'Espace Économique Européen (EEE)
</p>
<p>
L’accès aux données personnelles, que ce soit par les enseignants ou les administrateurs, se fait toujours par une connexion sécurisée.
</p>
<h1>Droits d’accès, rectification, suppression et portabilité</h1>
<p>
Conformément aux dispositions légales et réglementaires applicables, en particulier la loi n° 78-17 du 6 janvier 1978 modifiée relative à l'informatique, aux fichiers et aux libertés et du règlement européen n°2016/679/UE du 27 avril 2016 (applicable dès le 25 mai 2018), l'utilisateur dispose des droits suivants :
</p>
<ul>
   <li>Mettre à jour ou supprimer ses données en se connectant à votre compte et en configurant ses paramètres ;
   <li>Exercer son droit d’accès, pour connaître les données personnelles qui le concernent ;</li>
   <li>Demander la mise à jour de ses données, si celles-ci sont inexactes ;</li>
   <li>Demander la portabilité ou la suppression de ses données ;</li>
   <li>(Le cas échéant) Demander la suppression de son compte ;</li>
   <li>Demander la limitation du traitement de ses données ;</li>
   <li>S'opposer, pour des motifs légitimes, au traitement de ses données ;</li>
   <li>(Le cas échéant) S'opposer ou retirer son consentement à l’utilisation, par nos services,
   de ses coordonnées pour l’envoi de nos promotions et sollicitations via courriers électroniques, messages SMS, appels téléphoniques et courriers postaux. Ce droit demeure valable que les informations le concernant nous aient été transmises directement par lui ou par des partenaires tiers à qui l'utilisateur les a communiquées (dans ce cas, il faudra cliquer sur les liens de désinscription prévus dans nos courriels ou nous contacter dans les conditions ci après).</li>
</ul>
<p>
Ces différents droits sont à exercer soit en modifiant les paramètres de son compte, soit par courriel à l'adresse suivante : info@france-ioi.org.
</p>
<p>
Pour des raisons de sécurité et éviter toute demande frauduleuse, cette demande devra être accompagnée d'un justificatif d'identité. Le justificatif sera détruit une fois la demande traitée. 
</p>
<p>
Pour les besoin d'organisation du concours, les limitations temporaires suivantes s'appliquent aux droit d'accès et d'effacement :
</p>
<p>
Dans le cas où ces données sont relatives à la participation à un concours récent, l'accès aux réponses fournies, scores et classement pourra n'être transmis qu'après la clôture et annonce des résultats de ce concours.
</p>
<p>
Dans le cas où l'utilisateur est un élève ayant participé au concours dans le cadre de son établissement, cet établissement pourra être informé par France-ioi de la demande de suppression, et les données pourront lui être transmises avant leur suppression.
</p>
<p>
Par ailleurs, un établissement peut faire la demande d'accès ou suppression de l'ensemble des données personnelles d'utilisateurs ayant participé aux concours dans le cadre de cet établissement, et n'ayant pas poursuivi une participation hors cadre de l'établissement. Les participants ayant créé leur propre compte et ayant fourni à France-ioi un moyen de les contacter pourront cependant être prévenus, et auront la possibilité de demander la
conservation de leurs données par France-ioi.
</p>
<p>
Si un utilisateur ne souhaite pas/plus recevoir nos actualités et sollicitations (par téléphone, SMS, courrier postal ou électronique) et invitations, il a la faculté de nous l’indiquer dans le cadre du présent formulaire , de modifier vos choix en nous contactant dans les conditions évoquées ci-dessus ou, en modifiant les paramètres de son profil en ligne.
</p>
<h1>Informations complémentaires</h1>
<p>
Aucun des sites rattachés au concours n'utilise de cookies de longue durée.
</p>
<p>
Ils n'utilisent que des cookies à durée très réduite (moins de 3h), avec pour unique rôle de permettre à l'utilisateur de pouvoir retrouver sa session en cas de fermeture de son navigateur par erreur, ou rechargement de la page.
</p>

