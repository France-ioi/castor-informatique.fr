<h1>Condition de participation</h1>

<h2>Comment participer au Castor Informatique&nbsp;?</h2>
<p>
L'inscription doit être réalisée par un(e) enseignant(e) dit "coordinateur" qui prend la responsabilité d'organiser l'épreuve avec les différents enseignants de son établissement.
</p>

<h2>Qui peut participer au Castor Informatique&nbsp;?</h2>
<p>
Le concours est ouvert à tous les niveaux de collège et lycée français dans toutes les filières, et également au CM1-CM2 depuis 2015.
</p>


<h2>Peut-on inscrire des élèves de lycée professionnel&nbsp;?</h2>
<p>
Oui. Le concours est ouvert à toutes les filières (générales, technologiques, professionnelles).
</p>

<h2>Doit-on obligatoirement inscrire une classe entière&nbsp;?</h2>
<p>
Non. L'enseignant(e) peut choisir de faire participer un sous-ensemble de ses élèves, ou de faire participer lors d'une même session des élèves de plusieurs classes, mais nous encourageons d'inscrire un maximum d'élèves sans
distinction de goût et d'appétence <i>a priori</i> pour l'informatique. Le concours est ludique et ne demande aucun prérequis. Nous souhaitons
donner à tous une image différente de l'informatique et y compris aux filles !
</p>

<h2>Le concours est-il accessible aux élèves non-voyant(e)s&nbsp;?</h2>
<p>
Non malheureusement. Les premières années nous proposions une version texte adaptée du concours, mais qui n'ont été utilisées que par moins d'une dizaine d'élèves. Ces dernières années, les sujets sont devenus très interactifs et visuelle, et une version en texte pur n'aurait plus de sens.
</p>


<h1>Inscription des coordinateurs</h1>


<h2>Quand les inscriptions sont-elles ouvertes&nbsp;?</h2>
<p>Les inscriptions sont ouvertes toute l'année. Si vous êtes enseignant et souhaitez faire participer votre établissement, inscrivez-vous sur <a href='http://coordinateur.castor-informatique.fr'>coordinateur.castor-informatique.fr</a>. Vous pouvez à tout moment organiser des épreuves d'entraînement avec vos élèves sur les sujets des concours des années passées. La création des groupes pour le concours officiel ouvre généralement courant ou fin septembre.</p>

<h2>Quelle est la date limite d'inscription&nbsp;?</h2>
<p>Il n'y a pas de date limite d'inscription, et il sera encore possible de s'inscrire pendant la semaine du concours. Nous vous conseillons cependant de vous inscrire sans attendre comme coordinateur de votre établissement. Vous serez alors libre de définir ou modifier les détails de la participation de votre établissement.</p>

<h2>Doit-il y avoir un seul coordonnateur, ou coordinatrice, par établissement&nbsp;? </h2>
<p>
Il est préférable qu'il y ait un responsable qui coordonne le concours dans l'établissement, mais il est possible de se répartir la tâche, en étant plusieurs inscrits comme coordinateur, et en réglant dans l'interface les droits d'accès que vous donnez aux autres coordinateurs du même établissement.
</p>

<h2>Un enseignant peut-il être coordinateur de plusieurs établissements&nbsp;?</h2>
<p>
Oui. Si vous êtes dans cette situation, veuillez  nous contacter, par mail à <a href="mailto:info@castor-informatique.fr?subject=Castor-ajouter un établissement à mon compte&body=Bonjour,%0A%0AJe suis déjà coordinateur(trice) de l'établissement : <saisir le nom complet>.%0AJe souhaiterais &ecirc;tre coordinateur(trice) de l'établissement : <saisir le nom complet>. %0A Mon nom :  %0A%0A---------%0A(Vous recevrez une réponse prochainement)">info@castor-informatique.fr</a>
</p>


<h2>L'enseignant(e) coordinateur est-il(elle) obligé(e) d'utiliser une adresse de courriel académique&nbsp;?</h2>
<p>L'utilisation de votre adresse de courriel académique facilitera grandement notre tâche de vérification lors de votre enregistrement comme coordinateur/trice. Si vous n'avez pas d'adresse académique ou que vous ne pouvez pas l'utiliser, veuillez  nous contacter par mail à <a href="mailto:info@castor-informatique.fr?subject=Castor-adresse academique&body=Bonjour,%0A%0AJe souhaite &ecirc;tre coordinateur pour le concours Castor. Malheureusement, je n'ai pas ou ne peux pas utiliser mon adresse académique.%0A Mon nom : %0A Mon académie : %0A Mon statut : %0A Mon établissement :  %0A%0A---------%0A(Après envoi de ce message, veuillez vous inscrire sur la plate-forme)">info@castor-informatique.fr</a>. </p>



<h1>Déroulement de l'épreuve</h1>

<h2>Le concours demande-t-il une préparation des élèves&nbsp;?</h2>
<p>
Pas nécessairement. Aucun prérequis n'est nécessaire. Il est néanmoins possible de rejouer les <a href="http://concours.castor-informatique.fr?tab=home">anciens concours</a>, qui sont accompagnés de corrigés détaillés et d'une explication du contexte scientifique.
</p>


<h2>L'épreuve se déroule-t-elle un jour précis à une heure précise&nbsp;?</h2>
<p>
L'enseignant(e) organise les sessions sur les plages horaires qui lui conviennent, dans la semaine du concours. Il/elle donne une date et un horaire indicatifs au moment de la création de chaque groupe d'élèves qui participera au concours au même niveau et en même temps.
Attention à bien enregistrer plusieurs groupes si plusieurs ensembles d'élèves de même niveau participent à des moments différents.
</p>

<h2>Quelle est la durée de l'épreuve&nbsp;?</h2>
<p>
45 minutes maximum.
</p>

<h2>L'épreuve doit-elle être passée entièrement d'un seul coup&nbsp;?
</h2>
<p>
Oui. Le candidat ne peut pas participer au concours en plusieurs fois. Une fois sa session ouverte le chronomètre tourne, et ne peut pas être arrêté. Il n'est donc pas possible de s'interrompre et de terminer plus tard. Il est cependant possible de fermer son navigateur et de reprendre le concours immédiatement via un autre navigateur ou sur un autre poste, en cas de problème technique, mais le chronomètre continue à tourner pendant cette opération.
</p>

<h2>Les élèves peuvent-ils(elles) passer l'épreuve hors temps scolaire, depuis leur domicile par exemple&nbsp;?</h2>
<p>
Non. Pour des raisons d'équité pour l'ensemble des candidats du concours (plusieurs centaines de milliers sur toute la France), il est important qu'il soit passé dans des conditions similaires par tous ; c'est-à-dire sur le temps scolaire et sous l'encadrement d'un enseignant. </</p>

<h2>Pourquoi les élèves doivent-ils indiquer leur genre lors du concours&nbsp;?</h2>
<p>Le concours Castor souhaite &oelig;uvrer à la promotion de l'informatique et de la science du numérique et de ses métiers auprès de tous les jeunes et, en particulier, auprès des filles. Les données sur le genre, à fournir au début de la participation au concours,
 nous permettront de calculer les taux de participation des filles, et leurs taux de réussite, nous apportant certainement des arguments en faveur de l'intéressement des filles à l'informatique.</p>


