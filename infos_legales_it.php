<h1>DPMR - Protezione dei dati personali</h1>


<p>
Quando gli istituti e gli utenti partecipano alle attività QuickPi, vengono raccolte informazioni personali.
</p>
<p>
France-ioi s'impegna a rispettare tutte le leggi e le normative sulla protezione dei dati raccolti.
</p>
<h1>Dati raccolti e finalità della raccolta</h1>

<h3>Participanti alle attività</h3>
<b>Dati raccolti (obbligatori) :</b>
<ul>
   <li>Cognome, nome<br/>
   (può essere formito uno pseudonimo o un nome parziale, a scapito dei diplomi)</li>
   <li>Livello scolastico</li>
   <li>Istituto scolastico (indirettamente)</li>
   <li>Risposte agli esercizi</li>
</ul>
<br/>
<b>Finalità della raccolta:</b>
<ul>
   <li>Visualizzazione dei risultati degli studenti nell'interfaccia del coordinatore dell'istituto</li>
   <li>Identificazione delle partecipazioni interessate da un'anomalia riscontrate dall'insegnante</li>
</ul>

<h3>Insegnanti che coordinano l'attività</h3>
<b>Dati raccolti (obbligatori) :</b>
<ul>
   <li>Cognome, nome</li>
   <li>Titolo (Sig., Sig.ra, ...)</li>
   <li>Indirizzo e-mail</li>
   <li>Coordinate dell'istituto</li>
</ul>
<br/>
<b>Finalità della raccolta:</b>
<ul>
   <li>Verifica della condizione di insegnante</li>
   <li>Comunicazione diretta con l'insegnante</li>
   <li>Pubblicazione del numero di partecipanti per istituto</li>
</ul>

<p>
I dati personali trasmessi dagli utenti sono registrati in un file informatico dall'associazione France-ioi.
</p>
<p>Durante la partecipazione individuale ad un'attività ad accesso libero, vengono raccolte le risposte degli utenti agli esercizi,
e allegate ad un codice generato in modo casuale, che viene trasmesso all'utente.</p>

<p>Lo scopo di questa raccolta è soprattutto quello di permettere all'utente di continuare la sua attività in un secondo momento, fornendo il codice casuale che gli è stato trasmesso.</p>
<p>Uno scopo secondario è la rilevazione di statistiche sull'uso dei contenuti, sulla difficoltà degli esercizi, ecc. per il futuro miglioramento di questi contenuti.</p>

<p>Questi dati non contengono alcun dato personale, a meno che l'utente non scelga di includerli nelle sue risposte agli esercizi, sebbene ciò non sia affatto richiesto o suggerito. Tuttavia, poiché è possibile, trattiamo tutti i dati registrati con le stesse condizioni di sicurezza come se fossero dati personali.</p>


<p>
I dati forniti dagli utenti sono registrati in un file elaborato dall'associazione France-ioi.
</p>
<h1>Periodo di conservazione dei dati</h1>
<p>
Le risposte degli studenti agli esercizi sono conservate per un massimo di 18 mesi.
</p>

<h1>Privacy e sicurezza dei dati</h1>
<p>
Durante questo periodo, metteremo in atto tutti i metodi per garantire la riservatezza e la sicurezza di questi dati, in modo da prevenire il loro danneggiamento, la loro cancellazione o l'accesso da parte di terzi non autorizzati.
</p>
<p>
L'accesso a questi dati è strettamente limitato ad un massimo di 3 persone che lavorano per l'associazione France-ioi, autorizzate in ragione delle loro funzioni e vincolate da un obbligo di riservatezza: il presidente dell'associazione e due sviluppatori responsabili della manutenzione dei server .
</p>
<p>
France-ioi si impegna a non vendere, affittare, trasferire o dare a terzi l'accesso ai dati personali, a meno che non siano obbligati a farlo per un motivo legittimo (obbligo legale, lotta contro la frode o l'abuso, esercizio dei diritti di difesa, ecc.)
</p>
<p>
I dati personali trattati nell'ambito della nostra attività sono conservati solo su server sicuri appartenenti al leader mondiale dell'hosting di server, AWS, situato in Europa. I dati non saranno mai trasferiti in un paese al di fuori dello Spazio Economico Europeo (SEE).
</p>
<p>
L'accesso ai dati personali avviene sempre attraverso una connessione sicura.
</p>
<h1>Diritti di accesso, rettifica, cancellazione e portabilità</h1>
<p>
In conformità alle disposizioni legali e regolamentari applicabili, in particolare la legge n. 78-17 del 6 gennaio 1978 e successive modifiche relative all'informatica, agli archivi e alle libertà e il regolamento europeo n. 2016/679/UE del 27 aprile 2016 (applicabile dal 25 maggio 2018), l'utente gode dei seguenti diritti:
</p>
<ul>
   <li>Esercizio del diritto di accesso, per conoscere i dati personali che lo riguardano ;</li>
   <li>Richiedere un aggiornamento dei suoi dati, se non sono corretti ;</li>
   <li>Richiedere la portabilità o la cancellazione dei dati ;</li>
   <li>Richiedere la limitazione del trattamento dei suoi dati ;</li>
   <li>Opporsi, per motivi legittimi, al trattamento dei suoi dati ;</li>
</ul>
<p>
Questi diversi diritti devono essere esercitati contattandoci via e-mail al seguente indirizzo : info@france-ioi.org.
</p>
<p>
Poiché non conserviamo alcun dato personale, ci può essere fornito solo il codice casuale trasmesso all'utente, in modo da poter identificare quali dati sono interessati dalla richiesta.
</p>
<h1>Utilizzo dei cookies</h1>
<p>
Nessuno dei siti collegati al concorso utilizza cookie a lungo termine.
</p>
<p>
Essi utilizzano solo cookies di brevissima durata (meno di 3 ore), con l'unico scopo di permettere all'utente di recuperare la sessione in caso di chiusura del browser per errore, o di ricaricare la pagina.
</p>
<h1>Algoritmi di calcolo del punteggio</h1>
<h3>Calcolo del punteggio</h3>
   <p>
   Durante lo svolgimento di una partecipazione, il punteggio di ogni partecipante viene calcolato e mostrato in modo permanente.
   </p>
   <p>
   È semplicemente la somma dei punti ottenuti su ogni argomento.
   </p>

